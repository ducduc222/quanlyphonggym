    private void loadData() throws SQLException, ClassNotFoundException {
        HoiVienHomeUI.loadData();
        //load CombboxNhanVien
        jComboBox1.removeAllItems();
        List<NhanVienBean> nhanVienBeans = new ArrayList<>();
        NhanVienCtrl nhanVienCtrl = new NhanVienCtrl();
        nhanVienBeans = nhanVienCtrl.getAllNhanVien();
        for (int i = 0; i <nhanVienBeans.size(); i++) {
            String item = nhanVienBeans.get(i).getNhanVien().getId()+"-"+nhanVienBeans.get(i).getNhanVien().getHoTen()+"-"+nhanVienBeans.get(i).getRole().getTenRole();
            jComboBox1.addItem(item);
        }

        this.idHoiVien = HoiVienHomeUI.hoiVienBean.getHoiVien().getId();
        CRPhanHoiHoiVienCtrl crPhanHoiHoiVienCtrl = new CRPhanHoiHoiVienCtrl();
        List<PhanHoiBean> phanHoiBeanList  =crPhanHoiHoiVienCtrl.getAllPhanHoiCuaHoiVienById(idHoiVien);
//phanhoi
        if (danhSachPhanHoi.getRowCount()>0) danhSachPhanHoi.setRowCount(0);
        for (int i = 0; i<phanHoiBeanList.size(); i++) {
            int id = phanHoiBeanList.get(i).getPhanHoiCuaHoiVien().getId();
            int idNhanVien = phanHoiBeanList.get(i).getPhanHoiCuaHoiVien().getIdNhanVien();
            String yKienHoiVien = phanHoiBeanList.get(i).getPhanHoiCuaHoiVien().getNoiDung();
            String traLoi = null;
            String check = "Chưa trả lời";
            String ngayPhanHoi = phanHoiBeanList.get(i).getPhanHoiCuaHoiVien().getNgayPhanHoi();

            if (phanHoiBeanList.get(i).getPhanHoiCuaHoiVien().getIsDaPhanHoiLai()) {
                traLoi = phanHoiBeanList.get(i).getPhanHoiCuaAdmin().getNoiDung();
                check = "đã trả lời";
            }

            Object[] row = {id, idNhanVien, yKienHoiVien, traLoi, check, ngayPhanHoi};
            danhSachPhanHoi.addRow(row);
        }
    }

