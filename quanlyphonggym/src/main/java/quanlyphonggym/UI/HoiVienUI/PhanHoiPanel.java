        if (jTextAreaYKien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hãy nhập ý kiến phản hồi");
            return;
        }
        PhanHoiCuaHoiVien phanHoiCuaHoiVien = new PhanHoiCuaHoiVien();
        phanHoiCuaHoiVien.setIdHoiVien(idHoiVien);

        String str = (String) jComboBox1.getSelectedItem();
        String[] part = str.split("-");
        int idNhanVien = Integer.parseInt(part[0].trim());

        phanHoiCuaHoiVien.setIdNhanVien(idNhanVien);
        phanHoiCuaHoiVien.setNoiDung(jTextAreaYKien.getText());

        CRPhanHoiHoiVienCtrl crPhanHoiHoiVienCtrl = new CRPhanHoiHoiVienCtrl();
        if (crPhanHoiHoiVienCtrl.guiPhanHoi(phanHoiCuaHoiVien)) {
            loadData();
        }

